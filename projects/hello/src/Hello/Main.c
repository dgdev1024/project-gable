#include <Hello/Application.h>

// Static Variables ////////////////////////////////////////////////////////////////////////////////

static Hello_Application* s_App = NULL;

// Static Functions ////////////////////////////////////////////////////////////////////////////////

static void Hello_AtExit ()
{
    Hello_DestroyApplication(s_App);
}

// Main Function ///////////////////////////////////////////////////////////////////////////////////

int main ()
{
    atexit(Hello_AtExit);
    s_App = Hello_CreateApplication();
    Hello_StartApplication(s_App);
    return 0;
}